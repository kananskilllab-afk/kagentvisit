export function searchCities(query, limit = 10) {
  if (!query || query.length < 1) return [];
  const q = query.toLowerCase();
  const exact = [];
  const starts = [];
  const contains = [];
  for (const c of INDIAN_CITIES) {
    const name = c.city.toLowerCase();
    if (name === q) exact.push(c);
    else if (name.startsWith(q)) starts.push(c);
    else if (name.includes(q)) contains.push(c);
  }
  return [...exact, ...starts, ...contains].slice(0, limit);
}

export function getCityTier(cityName) {
  const found = INDIAN_CITIES.find(c => c.city.toLowerCase() === cityName.toLowerCase());
  return found?.tier || null;
}

export function getCityState(cityName) {
  const found = INDIAN_CITIES.find(c => c.city.toLowerCase() === cityName.toLowerCase());
  return found?.state || null;
}
